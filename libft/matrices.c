#include <libft.h>

void	identy_m44(float *f)
{
	ft_memset(f, 0, sizeof(float) * 16);
	f[0] = 1.0f;
	f[5] = 1.0f;
	f[10] = 1.0f;
	f[15] = 1.0f;
}

void	scale_m44(float s, float *m)
{
	m[0] = m[0] * s;
	m[5] = m[5] * s;
	m[10] = m[10] * s;
}

void	translation_m44(t_float4 *d, float *m)
{
	m[12] = (*d)[0];
	m[13] = (*d)[1];
	m[14] = (*d)[2];
	m[15] = (*d)[3];
}

void	quat_to_m44(t_float4 *q, float *m)
{
	float f[9];

	f[0] = (*q)[0] * (*q)[0];
	f[1] = (*q)[0] * (*q)[1];
	f[2] = (*q)[0] * (*q)[2];
	f[3] = (*q)[0] * (*q)[3];
	f[4] = (*q)[1] * (*q)[1];
	f[5] = (*q)[1] * (*q)[2];
	f[6] = (*q)[1] * (*q)[3];
	f[7] = (*q)[2] * (*q)[2];
	f[8] = (*q)[2] * (*q)[3];
	ft_memset(m, 0, sizeof(float) * 16);
	m[0] = 1.0f - 2.0f * (f[4] + f[7]);
	m[1] = 2.0f * (f[1] - f[8]);
	m[2] = 2.0f * (f[2] + f[6]);
	m[4] = 2.0f * (f[1] + f[8]);
	m[5] = 1.0f - 2.0f * (f[0] + f[7]);
	m[6] = 2.0f * (f[5] - f[3]);
	m[8] = 2.0f * (f[2] - f[6]);
	m[9] = 2.0f * (f[5] + f[3]);
	m[10] = 1.0f - 2.0f * (f[0] + f[4]);
	m[15] = 1.0f;
}
