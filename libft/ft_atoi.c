int	ft_atoi(const char *p)
{
	_Bool	minus;
	int		result;

	minus = 0;
	result = 0;
	while ((*p >= '\t' && *p <= '\r') || *p == ' ')
		++p;
	minus = *p == '-' ? 1 : 0;
	*p == '-' || *p == '+' ? ++p : p;
	while (*p >= '0' && *p <= '9')
	{
		result = result * 10 + *p - '0';
		++p;
	}
	return (minus ? -result : result);
}
